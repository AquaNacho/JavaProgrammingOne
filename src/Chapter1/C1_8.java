package Chapter1;

/**
 * Program to display the perimeter and area of a circle
 *
 * @author Garrett Bolding
 */
public class C1_8 {

    /**
     * Main Method
     *
     * @param args arguments from command line prompt
     */
    public static void main(String[] args) {
        System.out.println("Perimeter = " + (2 * 5.5 * Math.PI));
        System.out.println("Area = " + (Math.pow(5.5, 2) * Math.PI));
    }
}
